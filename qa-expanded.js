// ========== 增强版模糊匹配查找答案（含同义词展开） ==========
findAnswerByKeywords = function(question) {
    var rawQ = question.toLowerCase().trim();
    var cleanQ = removeStopWords(rawQ);
    // 同义词展开：将用户问题中的词替换为包含所有同义形式的扩展文本
    var expandedQ = expandQuery(rawQ);
    var expandedClean = removeStopWords(expandedQ);
    var i, j, k, item, keyword, savedQ, score, len;

    // ===== 垃圾答案检测函数（CORS代理常返回微软年报、公司财报等无关内容）=====
    var _isGarbageAnswer = (function() {
        var garbagePatterns = [
            '微软', 'Microsoft', 'microsoft', '年报', 'Annual Report', 'annual report',
            'NASDAQ', 'nasdaq', 'SEC', 'Form 10-K', '10-K', '财年',
            'fiscal year', 'Fiscal Year', '董事会', 'Board of Directors', '股东',
            'shareholder', 'Shareholder', 'Stock', 'stock', 'NYSE', 'nyse',
            'investor', 'Investor', '披露', 'disclosure', '合规', 'compliance',
            '收入', 'revenue', 'Revenue', '利润表', '资产负债表', '现金流量',
            'LinkedIn', 'linkedin', 'GitHub', 'github', '社招', '校招', '招聘'
        ];
        return function(answer) {
            if (!answer) return true;
            for (var gi = 0; gi < garbagePatterns.length; gi++) {
                if (answer.indexOf(garbagePatterns[gi]) !== -1) return true;
            }
            return false;
        };
    })();
    // ===== 垃圾检测函数结束 =====

    // ---- 第1层：用户学过的知识，精确匹配 ----
    var userLearned = getUserLearnedAnswers();
    for (i = 0; i < userLearned.length; i++) {
        savedQ = userLearned[i][0].toLowerCase().trim();
        if (savedQ === rawQ) {
            if (_isGarbageAnswer(userLearned[i][1])) {
                console.warn('[垃圾过滤] 用户学习数据含垃圾内容，已跳过:', savedQ);
                // 删除这条被污染的数据
                userLearned.splice(i, 1);
                i--;
                (function() {
                    try { localStorage.setItem('chatbot_user_learned', JSON.stringify(userLearned)); } catch(e) {}
                })();
                // 同时在云端也删除（异步）
                if (typeof supabaseDeleteQA === 'function') supabaseDeleteQA(savedQ);
                continue;
            }
            return userLearned[i][1];
        }
    }

    // ---- 第2层：用户学过的知识，子串匹配 + 模糊匹配 ----
    for (i = 0; i < userLearned.length; i++) {
        savedQ = userLearned[i][0].toLowerCase().trim();
        // 先检查是否垃圾内容
        if (_isGarbageAnswer(userLearned[i][1])) {
            console.warn('[垃圾过滤] 用户学习数据含垃圾内容，已跳过:', savedQ);
            userLearned.splice(i, 1);
            i--;
            (function() {
                try { localStorage.setItem('chatbot_user_learned', JSON.stringify(userLearned)); } catch(e) {}
            })();
            if (typeof supabaseDeleteQA === 'function') supabaseDeleteQA(savedQ);
            continue;
        }
        if (rawQ.indexOf(savedQ) !== -1 || savedQ.indexOf(rawQ) !== -1) {
            return userLearned[i][1];
        }
        if (savedQ.length >= 3 && cleanQ.length >= 3) {
            if (editSimilarity(cleanQ, savedQ) >= 0.75) {
                return userLearned[i][1];
            }
        }
    }

    // ---- 第3层：预置对话匹配 ----
    if (typeof presetConversations !== 'undefined') {
        for (i = 0; i < presetConversations.length; i++) {
            item = presetConversations[i];
            if (rawQ.indexOf(item.question.toLowerCase()) !== -1 ||
                item.question.indexOf(rawQ) !== -1) {
                return item.answer;
            }
        }
    }

    // ---- 第4层：知识库多策略模糊匹配（核心） ----
    var bestMatch = null;
    var bestScore = 0;
    var kw, es, ds, sub, x;

    for (i = 0; i < qaDatabase.length; i++) {
        item = qaDatabase[i];
        score = 0;

        for (j = 0; j < item.keywords.length; j++) {
            kw = item.keywords[j].toLowerCase();

            // 策略A：清洗后问题包含完整关键词（最高权重）
            if (cleanQ.indexOf(kw) !== -1) {
                score += kw.length * 14;
            } else if (expandedClean.indexOf(kw) !== -1) {
                score += kw.length * 12;
            }
            // 策略B：关键词包含清洗后问题
            else if (kw.indexOf(cleanQ) !== -1 && cleanQ.length >= 2) {
                score += cleanQ.length * 12;
            } else if (kw.indexOf(expandedClean) !== -1 && expandedClean.length >= 2) {
                score += expandedClean.length * 10;
            }
            // 策略C：原始问题/扩展问题直接子串匹配
            if (rawQ.indexOf(kw) !== -1) {
                score += kw.length * 11;
            } else if (kw.indexOf(rawQ) !== -1 && rawQ.length >= 2) {
                score += rawQ.length * 9;
            } else if (expandedQ.indexOf(kw) !== -1) {
                score += kw.length * 8;
            }

            // 策略D：编辑距离模糊匹配
            if (cleanQ.length >= 3 && kw.length >= 3) {
                es = editSimilarity(cleanQ, kw);
                if (es >= 0.8) { score += kw.length * 10 * es; }
                else if (es >= 0.7) { score += kw.length * 7 * es; }
                else if (es >= 0.55) { score += kw.length * 4 * es; }
            }
            if (expandedClean.length >= 3 && kw.length >= 3 && expandedClean !== cleanQ) {
                es = editSimilarity(expandedClean, kw);
                if (es >= 0.75) { score += kw.length * 8 * es; }
                else if (es >= 0.6) { score += kw.length * 5 * es; }
            }

            // 策略E：Bigram相似度
            if (cleanQ.length >= 3 && kw.length >= 3) {
                ds = diceSimilarity(cleanQ, kw);
                if (ds >= 0.6) { score += kw.length * 7 * ds; }
                else if (ds >= 0.45) { score += kw.length * 4 * ds; }
                else if (ds >= 0.3) { score += kw.length * 2 * ds; }
            }
            if (expandedClean.length >= 3 && kw.length >= 3 && expandedClean !== cleanQ) {
                ds = diceSimilarity(expandedClean, kw);
                if (ds >= 0.5) { score += kw.length * 5 * ds; }
                else if (ds >= 0.35) { score += kw.length * 3 * ds; }
            }

            // 策略F：滑动窗口子串匹配
            var maxSubLen = Math.min(cleanQ.length, kw.length, 6);
            for (len = 2; len <= maxSubLen; len++) {
                for (x = 0; x <= cleanQ.length - len; x++) {
                    sub = cleanQ.substring(x, x + len);
                    if (kw.indexOf(sub) !== -1) { score += sub.length * 2; }
                }
            }
            if (expandedClean !== cleanQ) {
                var maxSubLen2 = Math.min(expandedClean.length, kw.length, 6);
                for (len = 2; len <= maxSubLen2; len++) {
                    for (x = 0; x <= expandedClean.length - len; x++) {
                        sub = expandedClean.substring(x, x + len);
                        if (kw.indexOf(sub) !== -1) { score += sub.length * 1.5; }
                    }
                }
            }
        }

        if (score > bestScore) {
            bestScore = score;
            bestMatch = item;
        }
    }

    if (bestMatch && bestScore >= 18) {
        // ===== 答案内容相关性验证 =====
        // 提取查询中不被"通用建筑词汇"覆盖的特征词
        // 如果特征词完全不出现于答案内容中，可能是误匹配，应回退到网络搜索
        var genericTerms = ['建筑','特点','特色','结构','作用','功能','用途','历史','介绍','讲解','时期','时代','古代','中国','传统','什么','是','有','哪些','怎么','为何','如何','为什么','怎样'];
        var distinctive = cleanQ;
        for (var gi = 0; gi < genericTerms.length; gi++) {
            distinctive = distinctive.split(genericTerms[gi]).join('');
        }
        // 提取剩余部分的 bigram（2字词），在答案中查证
        if (distinctive.length >= 2) {
            var answerText = bestMatch.answer;
            var foundInAnswer = false;
            // 先检查是否有任意特征 bigram 出现在答案中
            var bigrams = new Set();
            for (var bi = 0; bi < distinctive.length - 1; bi++) {
                bigrams.add(distinctive.substring(bi, bi + 2));
            }
            for (var bg of bigrams) {
                if (answerText.indexOf(bg) !== -1) { foundInAnswer = true; break; }
            }
            // 也检查特征词是否直接出现在答案关键词中
            if (!foundInAnswer) {
                for (var ki = 0; ki < bestMatch.keywords.length; ki++) {
                    var kword = bestMatch.keywords[ki];
                    for (var bg of bigrams) {
                        if (kword.indexOf(bg) !== -1) { foundInAnswer = true; break; }
                    }
                    if (foundInAnswer) break;
                }
            }
            if (!foundInAnswer) {
                console.log('[相关性验证] 查询特征词"' + distinctive + '"不在答案中，疑似误匹配，回退网络搜索');
                bestMatch = null;
                bestScore = 0;
            }
        }
        // ===== 相关性验证结束 =====
    }
    if (bestMatch && bestScore >= 18) {
        return bestMatch.answer;
    }

    // ---- 第5层：在答案内容中搜索相关词汇（降权兜底） ----
    var words = rawQ.replace(/[吗呢吧呀啊么有什么|哪些|怎样|如何|为什么|是不是怎请问告诉我]/g, '')
        .split(/[,，.。!！?？\s、，：；（）()\/\\\-—…·~～""''（）【】《》<>{}[\]|&]/)
        .filter(function(w) { return w.length >= 2; });

    if (words.length > 0) {
        bestMatch = null;
        bestScore = 0;
        for (i = 0; i < qaDatabase.length; i++) {
            item = qaDatabase[i];
            score = 0;
            var al = item.answer.toLowerCase();
            for (j = 0; j < words.length; j++) {
                var w = words[j];
                if (al.indexOf(w) !== -1) { score += w.length * 4; }
            }
            if (score > bestScore) {
                bestScore = score;
                bestMatch = item;
            }
        }
        // 第5层也加相关性验证：去掉通用词后的特征词必须在答案中
        if (bestMatch && bestScore >= 6) {
            var genTerms5 = ['建筑','特点','特色','结构','作用','功能','用途','历史','介绍','讲解','时期','时代','古代','中国','传统','风格','类型','文化','艺术','技术','工艺','材料','形式','设计','空间','布局','环境','景观'];
            var dist5 = cleanQ;
            for (var g5 = 0; g5 < genTerms5.length; g5++) {
                dist5 = dist5.split(genTerms5[g5]).join('');
            }
            if (dist5.length >= 2) {
                var found5 = false;
                for (var dbi = 0; dbi < dist5.length - 1; dbi++) {
                    if (bestMatch.answer.indexOf(dist5.substring(dbi, dbi+2)) !== -1) { found5 = true; break; }
                }
                if (!found5) {
                    console.log('[qa-expanded第5层验证] 特征词不在答案中，放弃匹配');
                    bestMatch = null;
                    bestScore = 0;
                }
            }
        }
        if (bestMatch && bestScore >= 6) {
            return bestMatch.answer;
        }
    }

    return null;
};


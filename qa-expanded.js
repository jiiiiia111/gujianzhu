// 展开查询：将用户输入中的词替换为包含所有同义形式的扩展文本
function expandQuery(query) {
    var result = query;
    var keys = Object.keys(synonymMap);
    keys.sort(function(a, b) { return b.length - a.length; });
    for (var i = 0; i < keys.length; i++) {
        var k = keys[i];
        if (result.indexOf(k) !== -1) {
            var synonyms = synonymMap[k];
            for (var j = 0; j < synonyms.length; j++) {
                if (result.indexOf(synonyms[j]) === -1) {
                    result += ' ' + synonyms[j];
                }
            }
        }
    }
    return result;
}

// ========== 模糊匹配工具函数 ==========

function levenshtein(a, b) {
    var m = a.length, n = b.length, i, j;
    if (m === 0) return n;
    if (n === 0) return m;
    var dp = [];
    for (i = 0; i <= m; i++) { dp[i] = [i]; }
    for (j = 0; j <= n; j++) { dp[0][j] = j; }
    for (i = 1; i <= m; i++) {
        for (j = 1; j <= n; j++) {
            if (a[i - 1] === b[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            } else {
                dp[i][j] = 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
            }
        }
    }
    return dp[m][n];
}

function editSimilarity(a, b) {
    if (!a || !b) return 0;
    if (a === b) return 1;
    var maxLen = Math.max(a.length, b.length);
    return 1 - levenshtein(a, b) / maxLen;
}

function toBigrams(str) {
    var bigrams = new Set(), i;
    for (i = 0; i < str.length - 1; i++) {
        bigrams.add(str.substring(i, i + 2));
    }
    return bigrams;
}

function diceSimilarity(a, b) {
    var bga = toBigrams(a), bgb = toBigrams(b), x;
    if (bga.size === 0 && bgb.size === 0) return 0;
    var intersect = 0;
    for (x of bga) {
        if (bgb.has(x)) intersect++;
    }
    return (2 * intersect) / (bga.size + bgb.size);
}

